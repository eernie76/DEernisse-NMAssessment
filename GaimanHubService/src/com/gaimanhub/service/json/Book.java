package com.gaimanhub.service.json;

public class Book {
	private String title;
	private String authors;
	private String originalPublishDate;
	private String publisher;
	private String isbn;
	private String openLibraryLink;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAuthors() {
		return authors;
	}
	public void setAuthors(String authors) {
		this.authors = authors;
	}
	public String getOriginalPublishDate() {
		return originalPublishDate;
	}
	public void setOriginalPublishDate(String originalPublishDate) {
		this.originalPublishDate = originalPublishDate;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public String getOpenLibraryLink() {
		return openLibraryLink;
	}
	public void setOpenLibraryLink(String openLibraryLink) {
		this.openLibraryLink = openLibraryLink;
	}
	
}
